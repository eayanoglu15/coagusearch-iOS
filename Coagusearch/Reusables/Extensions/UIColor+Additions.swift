import Foundation
import UIKit

extension UIColor {
    
    @nonobjc class var clearBlue: UIColor {
       return UIColor(red: 45.0 / 255.0, green: 121.0 / 255.0, blue: 230.0 / 255.0, alpha: 1.0)
     }

     @nonobjc class var twilightBlue: UIColor {
       return UIColor(red: 9.0 / 255.0, green: 61.0 / 255.0, blue: 135.0 / 255.0, alpha: 1.0)
     }
    
    @nonobjc class var azul: UIColor {
      return UIColor(red: 38.0 / 255.0, green: 116.0 / 255.0, blue: 218.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var lightBlueGrey: UIColor {
      return UIColor(red: 185.0 / 255.0, green: 198.0 / 255.0, blue: 216.0 / 255.0, alpha: 1.0)
    }

    @nonobjc class var dodgerBlue: UIColor {
      return UIColor(red: 61.0 / 255.0, green: 139.0 / 255.0, blue: 1.0, alpha: 1.0)
    }
}


